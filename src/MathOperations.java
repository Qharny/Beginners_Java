import java.lang.Math;

public class MathOperations {
    public static void main(String[] args) {
        // Calculate the natural log of 45
        double logOf45 = Math.log(45);

        // Calculate the square root of 29
        double sqrtOf29 = Math.sqrt(29);

        // Calculate the square root of 565
        double sqrtOf565 = Math.sqrt(565);

        // Display the results
        System.out.println("Natural log of 45: " + logOf45);
        System.out.println("Square root of 29: " + sqrtOf29);
        System.out.println("Square root of 565: " + sqrtOf565);
    }
}
