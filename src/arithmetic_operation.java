public class arithmetic_operation {
    public static void main(String[] args) {
        int num1 = 28;
        int num2 = 30;
        double dec1 = 2.3;
        double dec2 = 3.5;

        // Addition
        System.out.println("The sum of two numbers are (num1 + num2 ): " + (num1 + num2));
        System.out.println("The sum of two numbers are (num1 + num2 ): " + (dec1 + num2));

        System.out.println("The multiplication of two numbers are (num1 + num2 ): " + (num1 * num2));

        System.out.println("The division of two numbers are (num1 / num2 ): " + (dec1/num2));

        System.out.println("The subtraction of two numbers are (num1 - num2 ): " + (num1 - dec2));


    }

}
